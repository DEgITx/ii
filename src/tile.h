// Сетка тайлов для image-to-image моделей с маленьким окном входа
// (FSRCNN 96×96, denoise/enhance с фиксированным размером и т.п.).
//
// Зачем оно надо. Универсальный раннер по умолчанию letterbox’ит весь
// кадр в окно модели — получается сильно сжатая картинка и (для SR)
// апскейл идёт от «маленького» к «крупному», но всё равно от того, что
// модель видела внутри 96×96. Tiling работает наоборот: исходный кадр
// разбивается на тайлы размером с вход модели, каждый тайл прогоняется
// отдельно, результаты сшиваются в полноразмерный выходной canvas.
// На выходе — реальное разрешение исходника (с учётом коэффициента
// апскейла модели), а FPS «один полный кадр == 1 такт».
//
// Модуль намеренно ничего не знает про Engine и про конкретный
// фреймворк — это «тайл-арифметика»: spec → layout → extract → paste →
// finalize. Сам цикл инференса и заливку входа собирает ii.cpp.
//
// Ограничения:
//   * предполагаем целочисленный апскейл выхода относительно входа
//     (scale_x = W_out / W_in, аналогично для H). Иначе stitching
//     становится неоднозначным — лучше сразу упасть с ошибкой;
//   * canvas хранит uint8 RGB (3 канала) — image_proc.cpp уже разворачивает
//     grayscale-выход в R=G=B, так что для всех поддерживаемых семейств
//     моделей сборка одинаковая.

#pragma once

#include <cstdint>
#include <vector>

#include "image_proc.h"

namespace tile {

// План разбиения source-кадра на тайлы. Все координаты в input-space
// (т.е. в пикселях исходного кадра, до апскейла модели).
//
// Логика выбора позиций:
//   * шаг по оси = tile - overlap;
//   * первая позиция = 0; следующие — каждые `шаг` пикселей, пока
//     тайл целиком помещается в source;
//   * последний тайл прижимается к правому/нижнему краю
//     (x = src_w - tile), чтобы покрыть остаток без чёрных полос —
//     даже если он перекрывается с предыдущим больше, чем overlap,
//     ничего не ломает (paste либо переписывает, либо взвешенно
//     смешивает);
//   * если src < tile, единственная позиция (0, 0); недостающие
//     пиксели тайла дополняются replicate-edge (см. extract_tile).
struct TileLayout {
    int src_w = 0, src_h = 0;
    int tile_w = 0, tile_h = 0;
    int overlap = 0;
    std::vector<int> x0;   // позиции тайлов по X (верхний-левый угол)
    std::vector<int> y0;   // позиции тайлов по Y
    int nx() const { return (int)x0.size(); }
    int ny() const { return (int)y0.size(); }
    int count() const { return nx() * ny(); }
};

// Спланировать сетку тайлов под (src_w × src_h) при заданном тайле и
// overlap. Функция чисто арифметическая, без аллокаций сверх векторов
// в результате — её можно звать в горячем пути (видео-цикл), но обычно
// достаточно вызвать один раз: при фиксированном размере источника
// layout не меняется.
TileLayout plan_tiles(int src_w, int src_h, int tile_w, int tile_h,
                      int overlap);

// Вырезать тайл из RGB-кадра. Если тайл выходит за границы (src < tile
// или последний тайл смещён в пределы), за границей используется
// replicate-edge (повтор крайнего пикселя) — избегаем чёрных полос
// и серых padding-артефактов, которые SR-сеть честно бы «улучшила».
// dst всегда имеет размер tile_w * tile_h * 3 и тип uint8 HWC.
void extract_tile(const uint8_t* src_rgb, int src_w, int src_h,
                  int x0, int y0, int tile_w, int tile_h,
                  std::vector<uint8_t>& dst);

// Canvas для сборки тайлов в финальный кадр. В двух режимах:
//   * blend=false — простой overwrite-paste. Тайлы пишутся как есть,
//     overlap-зоны перезаписываются последним пастом. Дёшево, без
//     float-буферов. Подходит для denoise/enhance с тщательным
//     overlap=0 (тайлы стыкуются ровно по сетке) или когда визуальные
//     швы не критичны.
//   * blend=true — линейное feathering: в overlap-зоне вес тайла
//     плавно (линейно) спадает от центра к краю, итог = взвешенная
//     сумма / суммарный вес. Требует двух float-буферов (acc и weight)
//     суммарно ~16 байт на пиксель canvas’а. Швы при разумном
//     overlap (4..16 пикселей input-space) становятся незаметны.
struct TileCanvas {
    int width = 0, height = 0;
    int overlap_out = 0;     // overlap * scale в output-space (для feathering)
    bool blend = false;

    std::vector<uint8_t> rgb;     // финальный кадр, HWC RGB (после finalize)
    std::vector<float>   acc;     // акк. сумма (w*h*3), только при blend
    std::vector<float>   weight;  // акк. вес  (w*h),   только при blend

    // Инвариант blend-режима: true ⇒ acc/weight гарантированно занулены и
    // готовы к накоплению. finalize() оставляет буферы чистыми (зануляет
    // каждую использованную ячейку сразу после деления), поэтому reset()
    // при неизменном размере canvas’а пропускает полный memset на ~42 МБ.
    // Сбрасывается в false, как только paste() начал накопление; если
    // проход прервался до finalize() — следующий reset() сделает полную
    // очистку, так что корректность не зависит от того, дошли ли мы до
    // finalize() в предыдущем кадре.
    bool blend_clean = false;

    // Подготовить canvas под пасты. Если размер не меняется — без
    // realloc’а; в blend-режиме acc/weight зануляются лишь при
    // (ре)аллокации или если предыдущий проход не финализирован
    // (см. blend_clean).
    void reset(int w, int h, int overlap_out_, bool blend_);

    // Вставить декодированный тайл в позицию (dst_x, dst_y) (это
    // output-space координаты, т.е. с учётом scale модели). В
    // blend-режиме весовая функция — произведение линейных ramp’ов
    // по обеим осям, длина ramp’а = overlap_out от каждого края тайла;
    // в blend=false режиме это простой byte-копир.
    void paste(const imgproc::OutputImage& tile, int dst_x, int dst_y);

    // Финализация: в blend-режиме делит acc на weight и пишет в rgb.
    // В blend=false режиме no-op (rgb уже заполнен paste’ом).
    void finalize();
};

}  // namespace tile
